    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String num = NoCuenta.getText();
        ResultSet Cuenta1 = null;
        if(num.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un numero de cuenta");
        }
        else{
            try {
                Statement sentencia = conexion.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY);
                Cuenta1 = sentencia.executeQuery("SELECT * FROM cuenta WHERE cuenta.Numero = '" + num + "';");
                if(Cuenta1.next()){
                    //this.setEnabled(false);
                    new Cuenta(conexion,Cuenta1).setVisible(true);
                    
                }
                else{
                    JOptionPane.showMessageDialog(null, "Numero de cuenta incorrecto");
                }
            } catch (SQLException ex) {
                Logger.getLogger(AccederCuenta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

